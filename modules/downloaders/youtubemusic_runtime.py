# -*- coding: utf-8 -*-
"""YouTube Music下载运行时逻辑。"""


async def download_youtubemusic_music_runtime(
    downloader, url: str, download_path: str, message_updater=None, status_message=None, context=None, *, logger
) -> dict:
    """下载YouTube Music"""
    return await download_youtubemusic_music_runtime(
        downloader,
        url,
        download_path,
        message_updater=message_updater,
        status_message=status_message,
        context=context,
        logger=logger,
    )
